import { createAsyncThunk, createSlice } from '@reduxjs/toolkit';
import { fetchAllProducts,fetchProductByFilters,fetchCategories,fetchBrands,fetchProductById,createProduct,updateProduct } from './ProductAPI';

const initialState = {
  products: [],
  categories:[],
  brands:[],
  status: 'idle',
  totalItems:0,
  selectedProduct:null
};
